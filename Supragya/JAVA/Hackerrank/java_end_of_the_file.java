import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class java_end_of_the_file {

    public static void main(String[] args) {
       Scanner scan = new Scanner(System.in);
       int count=0;
        while(scan.hasNext()){
            System.out.println((++count)+" "+scan.nextLine());
        }
    }
}